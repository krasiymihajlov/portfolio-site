// ─── Seed функция ─────────────────────────────────────────────────────────────

async function seedArticles() {
  console.log('🌱 Стартиране на seed за статии...\n');

  for (const article of articles) {
    // Проверяваме дали вече съществува статия с този slug
    const q = query(collection(db, 'blogArticles'), where('slug', '==', article.slug));
    const existing = await getDocs(q);

    if (!existing.empty) {
      console.log(`⚠️  Статията "${article.title}" вече съществува (slug: ${article.slug}). Пропускане.`);
      continue;
    }

    const now = Timestamp.now();
    await addDoc(collection(db, 'blogArticles'), {
      ...article,
      createdAt: now,
      updatedAt: now,
    });

    console.log(`✅ Добавена: "${article.title}"`);
  }

  console.log('\n✨ Готово!');
  process.exit(0);
}

seedArticles().catch((err) => {
  console.error('❌ Грешка:', err);
  process.exit(1);
});
